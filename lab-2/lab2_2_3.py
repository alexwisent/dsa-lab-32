# Задание 2.3
# 1. Считать с клавиатуры произвольную строку.
# 2. Удалить из строки все символы точки.
# 3. Вывести в консоль количество удаленных символов.

print("Задание 2.3: удаление точек из строки")

# Считываем строку
s = input("Введите строку: ")

# Считаем точки и собираем новую строку без точек
new_string = ""
removed_count = 0

for char in s:
    if char == ".":
        removed_count += 1
    else:
        new_string += char

# Вывод результатов
print("Исходная строка:", repr(s))
print("Строка после удаления точек:", repr(new_string))
print("Количество удалённых точек:", removed_count)